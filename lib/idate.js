function iDate(options, format) {
	
	options = options || {};
	var idate;
	
	function __construct(date, format) {
		this.parse(date, format);
	}
	
	// property
	this.mseconds = 0;
	this.seconds = 0;
	this.minutes = 0;
	this.hours = 0;
	this.date = 0;
	this.month = 0;
	this.year = 0;
	
	this.up = function() {
		this.mseconds = idate.getMilliseconds();
		this.seconds = idate.getSeconds();
		this.minutes = idate.getMinutes();
		this.hours = idate.getHours();
		this.date = idate.getDate();
		this.month = idate.getMonth();
		this.year = idate.getFullYear();
		
		return this;
	}
	
	this.set = function(arg) {
		if(arg instanceof Date) {
			idate = arg;
		} else {
			idate = new Date(arg);
		}
		
		this.up();
		
		return this;
	}
	
	this.get = function() {
		return idate.getTime();
	}
	
	this.clone = function() {
		return (new iDate()).set(this.get());
	}
	
	this.parse = function(date, format) {
		if(typeof date == 'number') {
			if(1329678080 < date/1000) {
				idate = new Date();
				idate.setTime(date);
			} else {
				idate = new Date();
				idate.setTime(date*1000);
			}
			
		} else if(typeof date == 'string') {
			if(format) {
				idate = Date.parseDate(date, format);
			} else {
				if(date) idate = new Date(date);
				else idate = new Date();
			}
		} else {
			idate = new Date();
		}
		
		this.up();
		
		return this;
	}
	
	this.diff = function(idate0, units) {
		if(!(idate0 instanceof iDate)) idate0 = new iDate(idate0);
		var time = idate0.get() - idate.getTime();
		switch(units) {
			case 'week': time = time/(1000*60*60*24*7); break;
			case 'day': time = time/(1000*60*60*24); break;
			case 'hour': time = time/(1000*60*60); break;
			case 'minute': time = time/(1000*60); break;
			case 'second': time = time/1000; break;
		}
		return time;
	}
	
	this.idiff = function(idate0, units) {
		if(!(idate0 instanceof iDate)) idate0 = new iDate(idate0);
		var time = idate.getTime() - idate0.get();
		switch(units) {
			case 'week': time = time/(1000*60*60*24*7); break;
			case 'day': time = time/(1000*60*60*24); break;
			case 'hour': time = time/(1000*60*60); break;
			case 'minute': time = time/(1000*60); break;
			case 'second': time = time/1000; break;
		}
		return time;
	}
	
	this.offset = function(units, value) {
		if(units && typeof units == 'object') {
			for(var i in units) {
				this.offset.call(this, i, units[i]);
			}
		} else {
			var time = 0;
			switch(units) {
				case 'week': time = value*1000*60*60*24*7; break;
				case 'day': time = value*1000*60*60*24; break;
				case 'hour': time = value*1000*60*60; break;
				case 'minute': time = value*1000*60; break;
				case 'second': time = value*1000; break;
			}
			this.set(this.get()+time);
		}
		
		return this;
	}
	
	this.getOffset = function(units, value) {
		return this.clone().offset(units, value);
	}
	
	this.toString = function(format) {
		if(!format) format = 'd.m.Y H:i';
		return Date.formatDate(format, idate);
	}
	
	this.toTime = function() {
		return Date.formatDate('H:i', idate);
	}
	
	this.toDate = function() {
		return Date.formatDate('d.m.Y', idate);
	}
	
	__construct.call(this, options, format);
	
}

iDate.diffExp = function(time, units) {
    var difftime = 0;
    switch(units) {
        case 'week': difftime = time*1000*60*60*24*7; break;
		case 'day': difftime = time*1000*60*60*24; break;
		case 'hour': difftime = time*1000*60*60; break;
		case 'minute': difftime = time*1000*60; break;
		case 'second': difftime = time*1000; break;
	}
	if(!time) difftime = 0;
	if(difftime < 0) difftime = Math.abs(difftime);
	
	var data = {week: 0, day: 0, hour: 0, minute: 0, second: 0};
	
	data.week = Math.floor(difftime/(1000*60*60*24*7));
	difftime -= data.week*(1000*60*60*24*7);
	data.day = Math.floor(difftime/(1000*60*60*24));
	difftime -= data.day*(1000*60*60*24);
	data.hour = Math.floor(difftime/(1000*60*60));
	difftime -= data.hour*(1000*60*60);
	data.minute = Math.floor(difftime/(1000*60)) || '00';
	difftime -= data.minute*(1000*60);
	data.second = Math.floor(difftime/1000) || '00';
	difftime -= data.second*1000;
	
	return data;
}

